num1 = 10
num2 = 5

def addition(num1, num2):
    add = num1 + num2
    return add

def sub(num1, num2):
    sub = num1 - num2
    return sub
def mul(num1, num2):
    mul = num1 * num2
    return mul

print(addition(num1, num2))
print(sub(num1, num2))
print(mul(num1, num2))

